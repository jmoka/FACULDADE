// Definindo uma função assíncrona que recebe um parâmetro 'dados'
async function dados(dados) {
    // Armazenando o valor recebido no parâmetro 'dados' em uma variável local 'Dados'
    const Dados = dados

    // Retornando o valor armazenado na variável 'Dados'
    return Dados
}

// Exportando a função 'dados' para que ela possa ser utilizada em outros módulos
module.exports = dados
